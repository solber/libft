#include "libft.h"

int		ft_strncmp(char *s1, const char *s2, size_t n)
{
	size_t		i;

	i = 0;
	if (n < 1)
		return (0);
	while (s1[i] == s2[i] && i < n - 1)
	{
		if (s1[i] == '\0')
			return (0);
		i++;
	}
	return (s1[i] - s2[i]);
}
