#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s++ != '\0')
		i++;
	while (*s-- != (char)c)
		if (i-- == 0)
			return (NULL);
	return ((char *)++s);
}
